class ArthmeticOperators{
	public static void main(String[] args){
		int a = 10;
		int b = 20;
		int c = 5;
		int d = 2;
		System.out.println("Addition Result : " + (a+b));
		System.out.println("Subtraction Result : " + (b-a));
		System.out.println("Multiplication Result : " + (c*d));
		System.out.println("Division Result : " + (a/d));
		System.out.println("Remainder Result : " + (a%d));
	}
}