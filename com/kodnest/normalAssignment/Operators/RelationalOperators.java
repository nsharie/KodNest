class RelationalOperators{
	public static void  main(String[] args){
		int a = 10;
		int b = 20;
		System.out.println("Equal To Result : " + (a==b));
		System.out.println("NotEqual To Result : " + (a!=b));
		System.out.println("Greater Than Result : " + (a>b));
		System.out.println("Lesser Than Result : " + (a<b));
		System.out.println("Greater Than or Equal To Result : " + (a>=b));
		System.out.println("Lesser Than or Equal To Result : " + (a<=b));
	}
}